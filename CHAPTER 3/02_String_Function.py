Name = "Supriya"

print(len(Name))
print(Name.endswith("ya")) # it will check whether the string ends with "ya" or not
print(Name.startswith("Su")) # it will check whether the string starts with "Su" or not
print(Name.find("pr")) # it will find the index of "pr" in the string
print(Name.count("a")) # it will count the number of "a" in the string
print(Name.upper()) # it will convert the string to upper case
print(Name.lower()) # it will convert the string to lower case
print(Name.replace("Supriya", "Priya")) # it will replace "Supriya" with "Priya"
print(Name.strip()) # it will remove any leading and trailing whitespaces