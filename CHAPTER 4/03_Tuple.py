# Tuple i s a immutable dat a type. It is similar to a list but it is immutable.
# It means we cannot change the elements of a tuple once it is created.
# Tuples are created using parentheses () and can contain different data types.
# They are often used to group related data together.
#example of tuple
# Creating a tuple with different data types
my_tuple = ("Vishal", 25, 5.9, True)
print(my_tuple)  # Output: ('Vishal', 25, 5.9, True)

# crete a tuple with single element
my_tuple = ("Mahi",)  # Note the comma
print(my_tuple)  # Output: ('Mahi',)
